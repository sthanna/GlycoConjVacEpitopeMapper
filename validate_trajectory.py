
import mdtraj as md
import os
import numpy as np

def validate_real_md_trajectory(traj_path, pdb_path):
    print(f"=== Validating Real MD Trajectory: {os.path.basename(traj_path)} ===")
    
    if not os.path.exists(traj_path):
        print(f"ERROR: Trajectory file not found: {traj_path}")
        return
    
    if not os.path.exists(pdb_path):
        print(f"ERROR: PDB file not found: {pdb_path}")
        return

    try:
        # Load topology first
        top = md.load_topology(pdb_path)
        protein_indices = top.select("protein")
        print(f"Selecting {len(protein_indices)} protein atoms from total {top.n_atoms} atoms.")
        
        # Load trajectory (protein only)
        traj = md.load_dcd(traj_path, top=pdb_path, atom_indices=protein_indices)
        print(f"Successfully loaded {traj.n_frames} frames with {traj.n_atoms} protein atoms.")
        
        # Superpose on backbone (CA)
        ca_indices = traj.topology.select("name CA")
        print(f"Aligning on {len(ca_indices)} CA atoms...")
        traj.center_coordinates()
        traj.superpose(traj, 0, atom_indices=ca_indices)
        
        # Calculate RMSF
        rmsf = md.rmsf(traj, traj, 0)
        
        avg_rmsf = np.mean(rmsf)
        max_rmsf = np.max(rmsf)
        
        print(f"Average RMSF: {avg_rmsf:.4f} nm")
        print(f"Max RMSF: {max_rmsf:.4f} nm")
        
        if avg_rmsf > 0:
            print("SUCCESS: Trajectory contains physical movement.")
        else:
            print("WARNING: Trajectory appears static (RMSF is zero).")
            
    except Exception as e:
        print(f"ERROR during validation: {e}")

if __name__ == "__main__":
    BASE_DIR = os.path.dirname(os.path.abspath(__file__))
    # Use the solvated topology generated by the engine
    TRAJ = os.path.join(BASE_DIR, "data", "simulations", "site_LYS157.0", "trajectory.dcd")
    PDB = os.path.join(BASE_DIR, "data", "simulations", "site_LYS157.0", "solvated.pdb")
    validate_real_md_trajectory(TRAJ, PDB)
